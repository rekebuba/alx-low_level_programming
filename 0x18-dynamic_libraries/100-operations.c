#include <stdio.h>
/**
 * add - adds two numbers
 *
 * @a: first number
 * @b: second number
 * Return: int
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtract two numbers
 *
 * @a: first number
 * @b: second number
 * Return: int
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 *
 * @a: first number
 * @b: second number
 * Return: int
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - divides two numbers
 *
 * @a: first number
 * @b: second number
 * Return: int
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - module of two numbers
 *
 * @a: first number
 * @b: second number
 * Return: int
 */
int mod(int a, int b)
{
	return (a % b);
}
